import time

countdown = 1
while countdown <= 10:
    print(f"{countdown}...")
    time.sleep(1)  
    countdown += 1

print(" Happy New Year!!!")

# 1 To 10
import time

countdowns = 10
time.sleep(2)
while countdowns >= 1:
    print(f"{countdowns}...")
    time.sleep(1)
    countdowns -= 1
print("Happy New Year!!!")